package com.table;

public class RandomNameInfo {
	/**
	 * 类型（1姓，2男名，3女名）
	 */
	private int type;
	
	/**
	 * 内容
	 */
	private String value;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
